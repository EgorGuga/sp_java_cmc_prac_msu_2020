package web.controller.util;

public class CourseAndFlow {
    private int Course;
    private int Flow;

    public void setCourse(int course) {
        this.Course = course;
    }

    public void setFlow(int flow) {
        this.Flow = flow;
    }

    public int getCourse() {
        return Course;
    }

    public int getFlow() {
        return Flow;
    }
}
